# Another interesting note is that $\pi_{n}$ has rather long common suffix with $\pi_{n - 1}$ for even $n$, with an exception of $n = 12$.

def f(x)
  max_value = x.max
  y = Array.new(max_value) { Array.new(x.length, "  ") }
  x.each_with_index { |a_i, i| y[max_value - a_i][i] = "x " }
  puts y.map { |a| a.join("") }
end

x.each { |i| f(i) }
